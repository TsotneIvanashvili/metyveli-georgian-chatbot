def build_system_prompt(
    mode: str,
    contexts: Sequence[dict],
    grammar_summary: str | None = None,
) -> str:
    sections = [BASE_SYSTEM_PROMPT, MODE_PROMPTS.get(mode, MODE_PROMPTS["learn"])]

    if grammar_summary:
        sections.append(
            "ლოკალური გრამატიკის ანალიზატორის შედეგი:\n" + grammar_summary
        )

    if contexts:
        formatted = []
        for index, item in enumerate(contexts, start=1):
            formatted.append(
                "\n".join(
                    [
                        f"[წყარო {index}] {item['title']}",
                        f"ავტორი: {item.get('author') or 'უცნობია'}",
                        f"ჟანრი: {item.get('genre') or 'ტექსტი'}",
                        f"ფრაგმენტი: {item['text']}",
                    ]
                )
            )
        sections.append(
            "საძიებო სისტემის მიერ ნაპოვნი კონტექსტი:\n\n"
            + "\n\n".join(formatted)
        )
    else:
        sections.append(
            "ლოკალურ ცოდნის ბაზაში შესაბამისი ფრაგმენტი ვერ მოიძებნა. "
            "არ შექმნა გამოგონილი ციტატა ან წყარო."
        )

    return "\n\n".join(sections)
